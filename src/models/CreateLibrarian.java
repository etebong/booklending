    private void txtPhonenoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhonenoKeyTyped
        //validate phone entry column
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_SPACE))) {
            getToolkit().beep();
            txtPhoneno.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "Please use digits (0---9) only ");
            txtPhoneno.setBackground(Color.white);
            evt.consume();

        } else {
            if (txtPhoneno.getText().length() >= 11) {
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, "Local Phone number should not exceed 11 characters");
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtPhonenoKeyTyped

