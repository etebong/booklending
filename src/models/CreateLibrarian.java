    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //validate entries are entered
        if (txtSurname.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Surname not Provided.");
            txtSurname.requestFocus();
        } else if (txtFirstname.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Firstname not Provided.");
            txtFirstname.requestFocus();
        } else if (txtMiddlename.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Middlename not Provided.");
            txtMiddlename.requestFocus();
        } else if (txtAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Address not Provided.");
            txtAddress.requestFocus();
        } else if (txtPhoneno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Phone number is required.");
            txtPhoneno.requestFocus();

        } else if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password is required.");
            txtUsername.requestFocus();

        } else if (pswPassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Password is required.");
            pswPassword.requestFocus();

        } else if (buttonGroup1.isSelected(null)) {
            JOptionPane.showMessageDialog(null, "Select  Gender");

        } else {
            //test validity of username
            String username = txtUsername.getText();
            if (!username_Exist(username)) {
                try {
                    admin = getData();

                    try {

                        int result;
                        result = admin.createNewRecord(admin);

                        if (result == 1) {
                            JOptionPane.showMessageDialog(null, "Successfully created a new Librarian....");

                            this.dispose();

                        } else {
                            JOptionPane.showMessageDialog(null, "Problemm creating a Librarian......");
                        }
                    } catch (Exception e1) {
                        e1.printStackTrace();

                    }
                } catch (Exception e2) {

                }//end of try

            } else {
                JOptionPane.showMessageDialog(null, "User name already in used");
                txtUsername.requestFocus();
            }
//enf of test validity

        }//end of else if
    }//GEN-LAST:event_btnSaveActionPerformed

