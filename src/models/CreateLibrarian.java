    private boolean username_Exist(String username) {
        String sql = "SELECT * FROM admin WHERE username =?";
        try {
            con = ConnectDb.connect();
            pst = con.prepareStatement(sql);

            pst.setString(1, username);

            rs = pst.executeQuery();

            if (rs.next()) {
                return true;
            } else {
                return false;
            }

        } catch (Exception e) {
            e.printStackTrace();
            return false;
        } finally {
            try {
                pst.close();
                rs.close();
                con.close();
            } catch (Exception e2) {
                e2.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error ....");
            }
            //return false;
        }
    }
